static unsigned int lcm_compare_id(void)
{
	unsigned char buffer[4];
	unsigned int array[16];

	SET_RESET_PIN(1);
	SET_RESET_PIN(0);
	MDELAY(1);

	SET_RESET_PIN(1);
	MDELAY(20);

	array[0] = 0x00043700;	// read id return four byte
	dsi_set_cmdq(array, 1, 1);

	read_reg_v2(0xA1, buffer, 4);
	pr_info("%s:edo_ft3519:id=0x%02x%02x%02x%02x\n", __func__, buffer[0], buffer[1], buffer[2], buffer[3]);
	if (LCM_ID_0 == buffer[0] && LCM_ID_1 == buffer[1] && LCM_ID_2 == buffer[2] && LCM_ID_3 == buffer[3]) {
		pr_info("%s:kernel:edo_ft3519 reg A1 matched.\n", __func__);
		return 1;
	}
	else {
		pr_debug("%s: edo_ft3519 reg A1 unmatched.\n", __func__);
		return 0;
	}
}


