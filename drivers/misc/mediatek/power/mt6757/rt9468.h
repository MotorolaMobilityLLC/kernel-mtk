#define RT9468_SHIFT_MIVR_EN	0

#define RT9468_MASK_MIVR	0xFE
#define RT9468_MASK_MIVR_EN	(1 << RT9468_SHIFT_MIVR_EN)

/* ========== CHG_CTRL7 0x07 ============ */
#define RT9468_SHIFT_ICHG	2

#define RT9468_MASK_ICHG	0xFC

/* ========== CHG_CTRL8 0x08 ============ */
