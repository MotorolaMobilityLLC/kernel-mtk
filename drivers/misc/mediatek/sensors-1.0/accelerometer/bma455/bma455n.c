/*!
 *	@brief This API enables the features of the sensor.
 */
static uint16_t feature_enable(uint8_t feature, uint8_t len, uint8_t *feature_config, struct bma4_dev *dev)
{
	uint8_t index = 0;
	uint16_t rslt;

	/* Enable Step detector */
	if (feature & BMA455N_STEP_DETR) {
		/* Step detector enable bit pos. is 1 bytes ahead of the
		base address */
		index = BMA455N_STEP_CNTR_OFFSET + 1;
		feature_config[index] = feature_config[index] | BMA455N_STEP_DETECTOR_EN_MSK;
	}

	/* Enable Step counter */
	if (feature & BMA455N_STEP_CNTR) {
		/* Step counter enable bit pos. is 1 byte ahead of the
		base address */
		index = BMA455N_STEP_CNTR_OFFSET + 1;
		feature_config[index] = feature_config[index] | BMA455N_STEP_CNTR_EN_MSK;
	}

	/* Enable Activity */
	if (feature & BMA455N_ACTIVITY) {
		/* Activity enable bit pos. is 1 byte ahead of the
		base address */
		index = BMA455N_STEP_CNTR_OFFSET + 1;
		feature_config[index] = feature_config[index] | BMA455N_ACTIVITY_EN_MSK;
	}

	/* Enable Orientation */
	if (feature & BMA455N_ORIENTATION) {
		/* Orientation enable bit pos. is in the base address */
		index = BMA455N_ORIENTATION_OFFSET;
		feature_config[index] = feature_config[index] | BMA455N_ORIENT_EN_MSK;
	}

	/* Enable significant motion */
	if (feature & BMA455N_SIG_MOTION) {
		/* Sigmotion enable bit pos. is in the base address */
		index = BMA455N_SIG_MOTION_OFFSET;
		feature_config[index] = feature_config[index] | BMA455N_SIG_MOTION_EN_MSK;
	}

	/* Write the feature enable settings in the sensor */
	rslt = bma455_write_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config, len, dev);

	return rslt;
}

/*!
 *	@brief This API disables the features of the sensor.
 */
static uint16_t feature_disable(uint8_t feature, uint8_t len, uint8_t *feature_config, struct bma4_dev *dev)
{
	uint8_t index = 0;
	uint16_t rslt;

	/* Disable Step detector */
	if (feature & BMA455N_STEP_DETR) {
		/* Step detector enable bit pos. is 1 bytes ahead of the
		base address */
		index = BMA455N_STEP_CNTR_OFFSET + 1;
		feature_config[index] = feature_config[index] & (~BMA455N_STEP_DETECTOR_EN_MSK);
	}

	/* Disable Step counter */
	if (feature & BMA455N_STEP_CNTR) {
		/* Step counter enable bit pos. is 1 byte ahead of the
		base address */
		index = BMA455N_STEP_CNTR_OFFSET + 1;
		feature_config[index] = feature_config[index] & (~BMA455N_STEP_CNTR_EN_MSK);
	}

	/* Disable Activity */
	if (feature & BMA455N_ACTIVITY) {
		/* Activity enable bit pos. is 1 byte ahead of the
		base address */
		index = BMA455N_STEP_CNTR_OFFSET + 1;
		feature_config[index] = feature_config[index] & (~BMA455N_ACTIVITY_EN_MSK);
	}

	/* Disable Orientation */
	if (feature & BMA455N_ORIENTATION) {
		/* Orientation enable bit pos. is in the base address */
		index = BMA455N_ORIENTATION_OFFSET;
		feature_config[index] = feature_config[index] & (~BMA455N_ORIENT_EN_MSK);
	}

	/* Disable significant motion */
	if (feature & BMA455N_SIG_MOTION) {
		/* Sigmotion enable bit pos. is in the base address */
		index = BMA455N_SIG_MOTION_OFFSET;
		feature_config[index] = feature_config[index] & (~BMA455N_SIG_MOTION_EN_MSK);
	}

	/* Write the configured settings in the sensor */
	rslt = bma455_write_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config, len, dev);

	return rslt;
}

