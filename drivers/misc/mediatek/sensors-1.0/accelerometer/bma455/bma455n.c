/*!
 * @brief This API sets the configuration for orientation feature of the sensor.
 */
uint16_t bma455n_set_orientation_config(const struct bma455n_orientation_config *orientation,
				struct bma4_dev *dev)
{
	/* variable used to return the status of communication result*/
	uint16_t rslt = BMA4_OK;
	uint8_t feature_config[BMA455N_FEATURE_SIZE] = {0,};
	uint8_t index = BMA455N_ORIENTATION_OFFSET;
	uint16_t orient_settings_1_lsb = 0;
	uint16_t orient_settings_1_msb = 0;
	uint16_t orient_settings_1 = 0;

	if (dev != NULL) {
		if (dev->chip_id == BMA455N_CHIP_ID) {
			rslt = bma455_read_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config,
					BMA455N_FEATURE_SIZE, dev);
			if (rslt == BMA4_OK) {
				/* Extract upside/down, mode, block and theta */
				orient_settings_1_lsb = feature_config[index];
				orient_settings_1_msb = feature_config[index + 1] << 8;
				orient_settings_1 = orient_settings_1_lsb | orient_settings_1_msb;
				orient_settings_1 = BMA4_SET_BITSLICE(orient_settings_1, BMA455N_ORIENT_UD,
									orientation->upside_down);
				orient_settings_1 = BMA4_SET_BITSLICE(orient_settings_1, BMA455N_ORIENT_MODE,
									orientation->mode);
				orient_settings_1 = BMA4_SET_BITSLICE(orient_settings_1, BMA455N_ORIENT_BLOCK,
									orientation->blocking);
				orient_settings_1 = BMA4_SET_BITSLICE(orient_settings_1, BMA455N_ORIENT_THETA,
									orientation->theta);
				/* Assign upside/down, mode, block and theta value in feature config array*/
				feature_config[index++] = BMA4_GET_LSB(orient_settings_1);
				feature_config[index++] = BMA4_GET_MSB(orient_settings_1);

				/* Assign hysteresis value in feature config array */
				feature_config[index++] = BMA4_GET_LSB(orientation->hysteresis);
				feature_config[index++] = BMA4_GET_MSB(orientation->hysteresis);

				/* Writes orientation settings value in the sensor */
				rslt = bma455_write_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config,
					BMA455N_FEATURE_SIZE, dev);
			}
		} else {
			rslt = BMA4_E_INVALID_SENSOR;
		}
	} else {
		rslt = BMA4_E_NULL_PTR;
	}

	return rslt;
}

/*!
 * @brief This API gets the configuration of orientation feature from the sensor
 */
uint16_t bma455n_get_orientation_config(struct bma455n_orientation_config *orientation,
				struct bma4_dev *dev)
{
	/* variable used to return the status of communication result*/
	uint16_t rslt = BMA4_OK;
	uint8_t feature_config[BMA455N_FEATURE_SIZE] = {0};
	uint8_t index = BMA455N_ORIENTATION_OFFSET;
	uint16_t orient_settings_lsb = 0;
	uint16_t orient_settings_msb = 0;
	uint16_t orient_settings = 0;

	if (dev != NULL) {
		if (dev->chip_id == BMA455N_CHIP_ID) {
			rslt = bma455_read_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config,
					BMA455N_FEATURE_SIZE, dev);
			if (rslt == BMA4_OK) {
				/* Extract upside/down, mode, block and theta */
				orient_settings_lsb = feature_config[index];
				orient_settings_msb = feature_config[index + 1] << 8;
				orient_settings = orient_settings_lsb | orient_settings_msb;
				orientation->upside_down = BMA4_GET_BITSLICE(orient_settings, BMA455N_ORIENT_UD);
				orientation->mode = BMA4_GET_BITSLICE(orient_settings, BMA455N_ORIENT_MODE);
				orientation->blocking = BMA4_GET_BITSLICE(orient_settings, BMA455N_ORIENT_BLOCK);
				orientation->theta = BMA4_GET_BITSLICE(orient_settings, BMA455N_ORIENT_THETA);

				/* Extract hysteresis */
				orient_settings_lsb = feature_config[index + 2];
				orient_settings_msb = feature_config[index + 3] << 8;
				orient_settings = orient_settings_lsb | orient_settings_msb;
				orientation->hysteresis = BMA4_GET_BITSLICE(orient_settings, BMA455N_ORIENT_HYST);
			}
		} else {
			rslt = BMA4_E_INVALID_SENSOR;
		}
	} else {
		rslt = BMA4_E_NULL_PTR;
	}

	return rslt;
}

